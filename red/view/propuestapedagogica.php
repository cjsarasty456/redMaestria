      <p>
        Se plantea el desarrollo del RED como estrategia didáctica digital teniendo en cuenta que la irrupción de la tecnología en la vida diaria ha afectado nuestra forma de comunicarnos, entretenernos y educarnos, permitiendo acceder a múltiples ambientes de aprendizaje y contenidos multimodales, abriendo la posibilidad a la participación a la sociedad del conocimiento de manera mucho más democrática. Autores como Mendoza et al.  (2013) afirman que el “aprendizaje móvil busca responder a la demanda educativa del siglo XXI” (p.31). y proporciona ventajas significativas como la flexibilidad para aprender en el momento y lugar elegidos; la personalización de las experiencias de aprendizaje; el alcance de aprendizajes significativos a través del diseño de ambientes instruccionales; el desarrollo o fortalecimiento de sus habilidades profesionales; y una mayor efectividad del aprendizaje al promover una atención activa. 
      </p>
      <p>
        Se propuso el desarrollo de tres guías académicas destinadas a fortalecer la competencia lectora en el factor literatura, aquí el docente y el estudiante encontrarán actividades digitales para promover la lectura crítica y creativa a partir de textos literarios del escritor colombiano Manuel Zapata Olivella. Cada actividad corresponde a uno de los tres niveles de lectura: literal, inferencial o crítico intertextual; así mismo corresponde a un Derecho Básico de aprendizaje. Es importante resaltar que se debe seguir en orden el desarrollo de las guías dado que, aunque se abordaron diferentes textos, las actividades pedagógicas van aumentando el nivel de complejidad y se sugiere que el poema que se abordará (texto que aparece como prólogo a la obra Changó, el gran putas) también se trabaje de manera secuencial, tal como lo se propone en las guías.
      </p>
      <p>
        Es importante mencionar que, según la búsqueda de los investigadores, los RED enfocados en la didáctica del género poético son inexistentes y solo se registran recursos de tipo enciclopédico para dicho género, por lo cual esta plataforma es innovadora dado que aporta elementos didácticos-digitales para el tratamiento pedagógico de la poesía. Además, estas guías apuntan a la hipertextualidad con otros textos del escritor Manuel Zapata Olivella y otros textos multimodales que contextualizan y nutren la lectura desde otras disciplinas como las ciencias sociales y el arte.
      </p>
      <p>
        Por último, este proyecto de investigación rinde un humilde homenaje al extraordinario escritor Manuel Zapata e intenta recuperar en el ámbito pedagógico su pensamiento libertario e intercultural.
